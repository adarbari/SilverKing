package com.ms.silverking.cloud.dht.common;

public class NamespacePropertiesPutException extends NamespacePropertiesOperationException {
  public NamespacePropertiesPutException() {
    super();
  }

  public NamespacePropertiesPutException(String message) {
    super(message);
  }

  public NamespacePropertiesPutException(Throwable cause) {
    super(cause);
  }

  public NamespacePropertiesPutException(String message, Throwable cause) {
    super(message, cause);
  }

  public NamespacePropertiesPutException(String message, Throwable cause, boolean enableSuppression,
      boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }
}
