package com.ms.silverking.cloud.dht.common;

public class NamespacePropertiesOperationException extends Exception {
  public NamespacePropertiesOperationException() {
    super();
  }

  public NamespacePropertiesOperationException(String message) {
    super(message);
  }

  public NamespacePropertiesOperationException(Throwable cause) {
    super(cause);
  }

  public NamespacePropertiesOperationException(String message, Throwable cause) {
    super(message, cause);
  }

  public NamespacePropertiesOperationException(String message, Throwable cause, boolean enableSuppression,
      boolean writableStackTrace) {
    super(message, cause, enableSuppression, writableStackTrace);
  }
}
